"""
    Crie um programa que leio dois numeros e mostre a soma entre eles
"""

x = float(input("Digite um numero (float) para soma: "))
y = float(input("Digite outro numero (float) para a soma: "))

soma = x + y

print(f"A soma de {x} e {y} é {soma}")

